#include <config.h>

#include <stdio.h>

#if HAVE_STDLIB_H
#include <stdlib.h>
#endif /* HAVE_STDLIB_H */

#if HAVE_STRING_H
#include <string.h>
#endif /* HAVE_STRING_H */

#include <freetds/utils.h>

char *
tds_xstrdup(const char *s)
{
	char *p = strdup(s);

	if (!p) {
		fprintf(stderr, "Out of memory!\n");
		exit(EXIT_FAILURE);
	}
	return p;
}
